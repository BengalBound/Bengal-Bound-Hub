class FootwearProductionSchedule(models.Model):
    """12-day production plan for a footwear batch, mapping to the Master Production Sheet."""
    FOOTWEAR_TYPE = [
        ('sneakers', 'Sneakers'),
        ('sandals', 'Sandals'),
        ('ladies_heels', 'Ladies Heels'),
        ('oxfords', 'Formal Oxfords'),
        ('loafers', 'Penny Loafers'),
        ('boots', 'Boots'),
        ('other', 'Other'),
    ]

    business = models.ForeignKey('bredbound.BusinessInstance', on_delete=models.CASCADE, related_name='footwear_schedules')
    production_order = models.ForeignKey(
        ProductionOrder, on_delete=models.SET_NULL, null=True, blank=True, related_name='footwear_schedules'
    )
    article_code = models.CharField(max_length=50, blank=True)
    buyer = models.CharField(max_length=150, blank=True)
    footwear_type = models.CharField(max_length=20, choices=FOOTWEAR_TYPE, default='other')
    total_pairs_planned = models.PositiveIntegerField(default=0)
    start_date = models.DateField()
    created_by = models.ForeignKey('accounts.User', on_delete=models.SET_NULL, null=True, related_name='footwear_schedules_created')
    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        ordering = ['-start_date']

    def __str__(self):
        return f"{self.article_code or 'FW Schedule'} — {self.start_date} ({self.get_footwear_type_display()})"

    @property
    def overall_progress_pct(self):
        total_actual = sum(e.actual_output for e in self.day_entries.all())
        if self.total_pairs_planned:
            return min(100, int(total_actual / self.total_pairs_planned * 100))
        return 0


class ProductionDayEntry(models.Model):
    """Daily output log for one day within a FootwearProductionSchedule."""
    STAGE = [
        ('pre_d3', 'D-3 Pre-Production'),
        ('pre_d2', 'D-2 Pre-Production'),
        ('pre_d1', 'D-1 Pre-Production'),
        ('cutting', 'Cutting'),
        ('closing', 'Closing / Sewing'),
        ('lasting', 'Lasting'),
        ('bottoming', 'Bottoming'),
        ('finishing', 'Finishing'),
        ('qc', 'Final QC'),
        ('packout', 'Pack-out'),
    ]

    schedule = models.ForeignKey(FootwearProductionSchedule, on_delete=models.CASCADE, related_name='day_entries')
    day_label = models.CharField(max_length=10, help_text="e.g. D-3, D-2, 1, 2 … 12")
    production_date = models.DateField(null=True, blank=True)
    stage = models.CharField(max_length=15, choices=STAGE)
    key_activities = models.TextField(blank=True)
    daily_target = models.PositiveIntegerField(default=0)
    actual_output = models.PositiveIntegerField(default=0)
    cumulative_target = models.PositiveIntegerField(default=0)
    cumulative_actual = models.PositiveIntegerField(default=0)
    owner = models.CharField(max_length=100, blank=True)
    bottleneck = models.TextField(blank=True)

    class Meta:
        ordering = ['day_label']

    def __str__(self):
        return f"Day {self.day_label} — {self.get_stage_display()}"

    @property
    def variance(self):
        return self.actual_output - self.daily_target

    @property
    def on_target(self):
        return self.actual_output >= self.daily_target
