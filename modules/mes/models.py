class StationTrackingLog(models.Model):
    production_order = models.ForeignKey(ProductionOrder, on_delete=models.CASCADE, related_name='station_logs')
    station = models.ForeignKey(RoutingStation, on_delete=models.CASCADE)
    assigned_machine = models.ForeignKey('asset_management.Asset', on_delete=models.SET_NULL, null=True, blank=True, help_text="Specific machine assigned during this station step")
    worker = models.ForeignKey('accounts.User', on_delete=models.SET_NULL, null=True)
    entered_at = models.DateTimeField(default=timezone.now)
    exited_at = models.DateTimeField(null=True, blank=True)

    class Meta:
        ordering = ['-entered_at']

    def __str__(self):
        return f"{self.production_order} at {self.station}"

    @property
    def cycle_time_minutes(self):
        if self.exited_at:
            return int((self.exited_at - self.entered_at).total_seconds() / 60)
        return None


