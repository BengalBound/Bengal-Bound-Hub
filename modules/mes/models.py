from django.db import models
from django.utils import timezone


class WorkCenter(models.Model):
    STATUS = [('active', 'Active'), ('idle', 'Idle'), ('maintenance', 'Under Maintenance'), ('offline', 'Offline')]
    business = models.ForeignKey('bredbound.BusinessInstance', on_delete=models.CASCADE, related_name='mes_work_centers')
    code = models.CharField(max_length=20)
    name = models.CharField(max_length=100)
    description = models.TextField(blank=True)
    status = models.CharField(max_length=20, choices=STATUS, default='idle')
    capacity_per_hour = models.DecimalField(max_digits=8, decimal_places=2, default=0)
    operator = models.ForeignKey('bredbound.BusinessEmployee', on_delete=models.SET_NULL, null=True, blank=True)
    machine_id = models.CharField(max_length=50, blank=True)
    location = models.CharField(max_length=100, blank=True)
    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        unique_together = [('business', 'code')]
        ordering = ['code']

    def __str__(self):
        return f"{self.code} — {self.name}"

class Routing(models.Model):
    business = models.ForeignKey('bredbound.BusinessInstance', on_delete=models.CASCADE, related_name='mes_routings')
    name = models.CharField(max_length=100)
    description = models.TextField(blank=True)
    is_active = models.BooleanField(default=True)
    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        ordering = ['name']

    def __str__(self):
        return self.name


class RoutingStation(models.Model):
    routing = models.ForeignKey(Routing, on_delete=models.CASCADE, related_name='stations')
    work_center = models.ForeignKey(WorkCenter, on_delete=models.CASCADE, related_name='routing_stations')
    sequence = models.IntegerField(default=10)
    name = models.CharField(max_length=100, blank=True, help_text="Override work center name for this specific routing step")
    is_bottleneck = models.BooleanField(default=False)

    class Meta:
        ordering = ['routing', 'sequence']

    def __str__(self):
        return f"{self.routing.name} - Step {self.sequence}: {self.work_center.name}"


class ProductionOrder(models.Model):
    STATUS = [
        ('planned', 'Planned'), ('released', 'Released'), ('in_progress', 'In Progress'),
        ('paused', 'Paused'), ('completed', 'Completed'), ('cancelled', 'Cancelled'),
    ]
    PRIORITY = [('low', 'Low'), ('normal', 'Normal'), ('high', 'High'), ('urgent', 'Urgent')]

    business = models.ForeignKey('bredbound.BusinessInstance', on_delete=models.CASCADE, related_name='mes_production_orders')
    order_number = models.CharField(max_length=30)
    product_name = models.CharField(max_length=150)
    product_code = models.CharField(max_length=50, blank=True)
    output_lot = models.ForeignKey('inventory.ProductLot', on_delete=models.SET_NULL, null=True, blank=True, related_name='mes_production_orders', help_text="The batch/lot number generated by this order")
    work_center = models.ForeignKey(WorkCenter, on_delete=models.SET_NULL, null=True, blank=True, related_name='production_orders')
    routing = models.ForeignKey(Routing, on_delete=models.SET_NULL, null=True, blank=True, related_name='production_orders')
    current_station = models.ForeignKey(RoutingStation, on_delete=models.SET_NULL, null=True, blank=True, related_name='active_orders')
    quantity_planned = models.DecimalField(max_digits=12, decimal_places=3)
    quantity_produced = models.DecimalField(max_digits=12, decimal_places=3, default=0)
    unit = models.CharField(max_length=20, default='pcs')
    status = models.CharField(max_length=20, choices=STATUS, default='planned')
    priority = models.CharField(max_length=10, choices=PRIORITY, default='normal')
    scheduled_start = models.DateTimeField(null=True, blank=True)
    scheduled_end = models.DateTimeField(null=True, blank=True)
    actual_start = models.DateTimeField(null=True, blank=True)
    actual_end = models.DateTimeField(null=True, blank=True)
    created_by = models.ForeignKey('accounts.User', on_delete=models.SET_NULL, null=True)
    created_at = models.DateTimeField(auto_now_add=True)
    notes = models.TextField(blank=True)

    class Meta:
        unique_together = [('business', 'order_number')]
        ordering = ['-created_at']

    def __str__(self):
        return f"WO-{self.order_number}: {self.product_name}"

    @property
    def progress_percent(self):
        if self.quantity_planned > 0:
            return min(100, int((self.quantity_produced / self.quantity_planned) * 100))
        return 0


