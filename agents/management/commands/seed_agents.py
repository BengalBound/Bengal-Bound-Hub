class Command(BaseCommand):
    help = "Seeds the AgentCatalog with the 30 planned AI employees."

    def handle(self, *args, **kwargs):
        self.stdout.write("Seeding AI Agent Catalog...")
        created_count = 0
        updated_count = 0

        for agent_data in ALL_AGENTS:
            agent, created = AgentCatalog.objects.update_or_create(
                slug=agent_data["slug"],
                defaults=agent_data
            )
            if created:
                created_count += 1
            else:
                updated_count += 1

        self.stdout.write(
            self.style.SUCCESS(
                f"Completed seeding AI Agent Catalog. Created: {created_count}, Updated: {updated_count}."
            )
        )
